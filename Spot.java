public class Spot {
    private int x;
    private int y;
    //constructor
    public Spot(int x, int y) {
        this.x = x;
        this.y = y;
    }
    //accessors
    public int getX() {
        return this.x;
    }
    public int getY() {
        return this.y;
    }
    //mutators
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
}
